IF OBJECT_ID('TEMPDB.DBO.#TESTE','U') IS NOT NULL
DROP TABLE #TESTE

SELECT  TCA.ID_CLIENTE , 
		TCA.ID_USUARIO, 
		TU.NM_USUARIO,
		TU.NM_LOGIN_DISCADOR,
		TCA.NU_ATRASO_ORIGINAL,
		FAIXA = CASE    WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN -99999 AND 5 THEN '01 - Até 5 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 6 AND 7 THEN '02 - Entre 6 e 7 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 8 AND 15 THEN '03 - Entre 8 e 15 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 16 AND 30 THEN '04 - Entre 16 e 30 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 31 AND 60 THEN '05 - Entre 31 e 60 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 61 AND 69 THEN '06 - Entre 61 e 69 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 70 AND 90 THEN '07 - Entre 70 e 90 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 91 AND 180 THEN '08 - Entre 91 e 180 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 181 AND 270 THEN '09 - Entre 181 e 270 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 271 AND 360 THEN '10 - Entre 271 e 360 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 361 AND 720 THEN '11 - Entre 361 e 720 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 721 AND 1080 THEN '12 - Entre 721 e 1080 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 1081 AND 1440 THEN '13 - Entre 1081 e 1440 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 1441 AND 2160 THEN '14 - Entre 1441 e 2160 Dias'
						WHEN TCA.NU_ATRASO_ORIGINAL BETWEEN 2161 AND 99999 THEN '15 - Entre 2161 e 2520 Dias'
				END,
		TCA.VL_SALDO_ORIGINAL,
		DATA = CAST(TCA.DT_ACAO AS DATE),
		MES = DATEPART(MONTH,TCA.DT_ACAO )
INTO #TESTE		 
FROM TB_CLIENTE_ACAO TCA  WITH(NOLOCK)
	INNER JOIN TB_CLIENTE TC WITH(NOLOCK)
	ON TCA.ID_CLIENTE = TC.ID_CLIENTE
		INNER JOIN TB_USUARIO TU WITH(NOLOCK)
		ON TCA.ID_USUARIO = TU.ID_USUARIO
WHERE DATEPART(DAY,TCA.DT_ACAO ) BETWEEN  1 AND 20
AND DATEPART(MM,TCA.DT_ACAO ) IN (1,2,3,4,5,6)
AND DATEPART(YEAR,TCA.DT_ACAO ) = '2021'
AND TCA.ID_ACAO = 303
AND TC.ID_CEDENTE = 1

SELECT *
FROM #TESTE
ORDER BY  DATA



 


